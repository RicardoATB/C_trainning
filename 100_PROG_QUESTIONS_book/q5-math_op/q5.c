/* Book solutions 'C Programming - Beginner to Expert in 100 questions (Ansh Narad)'
 * Question: Write a program to show various arithmetic operations  */

#include <stdio.h>

int main (void)
{
	int a = 11;
	int b = 3;

	printf("sum of a and b is %d\n", a + b);
	printf("difference between a and b is %d\n", a - b);
	printf("product of a and b is %d\n", a*b);
	printf("a divided by b is %d\n", a/b);
	printf("reminder when a is divided by b is %d\n", a%b);
	
	return 0;
}
